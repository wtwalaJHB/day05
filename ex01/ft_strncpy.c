#include <unistd.h>


char *ft_strncpy(char *dest, char *src, unsigned int n)
{
	n = 0;

	while(src[n] != '\0')

	{
		dest[n] = src[n];
		dest[n +1] = '\0';
		n++;
	}

	return dest;
}
